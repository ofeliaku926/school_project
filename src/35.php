<?php
// This is just an example of how you might write PHP code.
// Feel free to modify it as needed!

// Example 1: Basic print statement

echo "Hello, World!";

// Example 2: Array access and manipulation

$fruits = ['apple', 'banana', 'cherry'];
echo $fruits[0]; // Outputs 'apple'
echo $fruits[1]; // Outputs 'banana'

// Example 3: Incrementing a variable
$number = 5;
$number++; // Updates the value of $number to 6

// Example 4: String concatenation

$a = "Hello, ";
$b = "world!";
echo $a . $b; // Outputs "Hello, world!"

// Example 5: Conditional statements

if ($condition) {
    echo "Condition is true.";
} else {
    echo "Condition is false.";
}

// Example 6: Loops
for ($i = 0; $i < 10; $i++) {
    echo "$i" . PHP_EOL;
}
